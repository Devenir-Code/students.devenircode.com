// Review of JavaScript Syntax, Operators, Conditional Statements, Loops, and Functions

// Variables and Operators
let a = 5;
let b = 10;
let c = a + b;
console.log(c);

// Conditional Statements
if (a < b) {
  console.log("a is less than b");
} else {
  console.log("a is greater than or equal to b");
}

// Loops
for (let i = 0; i < 5; i++) {
  console.log(i);
}

// Functions
function add(x, y) {
  return x + y;
}


let result = add(3, 4);
console.log(result);
